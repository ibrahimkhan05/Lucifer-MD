const { ytmp3, search } = require('@vreden/youtube_scraper');

exports.run = {
   usage: ['play'],
   hidden: ['lagu', 'song'],
   use: 'query',
   category: 'downloader',
   async: async (m, {
      client,
      text,
      isPrefix,
      command,
      env,
      users,
      Func
   }) => {
      try {
         if (!text) return client.reply(m.chat, Func.example(isPrefix, command, 'lathi'), m)

         client.sendReact(m.chat, 'ðŸ•’', m.key)

         const json  = await  search(text);
         const firstResp = json.results[0]
         if (!firstResp) return client.reply(m.chat, '*Song not found ðŸ˜“*', m)
         const downResult =  await ytmp3(firstResp.url, "320");
         
   

         let caption = `ä¹‚  *Y T - P L A Y*\n\n`
         caption += `â—¦ *Title* : ${firstResp.title}\n`
         caption += `â—¦ *Duration* : ${firstResp.duration.timestamp}\n`
         caption += `â—¦ *Views* : ${firstResp.views}\n`
         caption += `â—¦ *Channel* : ${firstResp.author.name}\n`
         caption += `â—¦ *Views* : ${firstResp.views}\n`
         caption += `â—¦ *Uploaded* : ${firstResp.ago}\n\n`
         caption += global.footer

         

         await client.sendMessageModify(m.chat, caption, m, {
            largeThumb: true,
            thumbnail: firstResp.thumbnail
         })

         await client.sendFile(m.chat, downResult.download.url, `${firstResp.title}.mp3`, '', m, {
            document: true,
            APIC: firstResp.thumbnail
         })

      } catch (e) {
         console.error(e)
         client.reply(m.chat, Func.jsonFormat(e), m)
      }
   },
   error: false,
   limit: true,
   restrict: true,
   cache: true,
   location: __filename
}
